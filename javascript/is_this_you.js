let canvas;
let width = 1268;
let height = 540;

let detections = [];
let video = document.getElementById('video');

let date = new Date();
let day = date.getDate();
let month = date.getMonth() + 1;
let year = date.getFullYear();
let hours = date.getHours();
let minutes = date.getMinutes();

let personPicture;
let faceCanvas;
let maskCanvas;
let blurH, blurV;
let pass1, pass2;
let maskBluredCanvas;
let shaderMask;

function preload(){
    let preloadFileName = 'assets/personPictures/' +  'emotionsPersonPicture_' + day + '_' + month + '_' + year + '_' + hours + '_' + minutes + '.png';

    personPicture = loadImage(preloadFileName);
    blurH = loadShader('/javascript/base.vert', '/javascript/blur.frag');
    blurV = loadShader('/javascript/base.vert', '/javascript/blur.frag');
    shaderMask = loadShader('/javascript/base.vert', '/javascript/shaderMask.frag');
}





function setup(){
    canvas = createCanvas(width, height, WEBGL);
    canvas.id('canvas');
    colorMode(HSB);

    const faceMesh = new FaceMesh({locateFile: (file) => {
        return `https://cdn.jsdelivr.net/npm/@mediapipe/face_mesh/${file}`;
    }});
      
    faceMesh.setOptions({
        maxNumFaces: 1,
        minDetectionConfidence: 0.1,
        minTrackingConfidence: 0.1,
    });
    
    faceMesh.onResults(gotFaces);

    let camera = new Camera(video, {
        onFrame: async () => {
            await faceMesh.send({image: video});
        },
        
        deviceId: 'fb15de7d86667efda375641fc381b7b897666690c0419045c82e3f7c1b922218',
        width: 1268,
        height: 540
    });
          
    camera.start();

    faceCanvas = createGraphics(width, height, WEBGL);
    maskCanvas = createGraphics(width, height, WEBGL);
    maskBluredCanvas = createGraphics(width, height, WEBGL);

    pass1 = createGraphics(width, height, WEBGL);
    pass2 = createGraphics(width, height, WEBGL);
    pass1.noStroke();
    pass2.noStroke();
}

      
function gotFaces(results){
  detections = results;
}





function draw(){
    clear();
    translate(- width/2, - height/2);
    if(detections != undefined ){
        if(detections.multiFaceLandmarks != undefined && detections.multiFaceLandmarks.length >= 1){
            displayFace();
        }
    }
}





function displayFace(){

    maskCanvas.clear();
    maskCanvas.push();
    maskCanvas.translate(-width / 2, -height / 2);

    maskCanvas.beginShape();

    maskCanvas.fill(255);
    maskCanvas.background(0);
    maskCanvas.noStroke();

    for(a = 0; a < tassellationDatabase.length; a++){
        let vertex1 = detections.multiFaceLandmarks[0][tassellationDatabase[a][0]];
        let vertex2 = detections.multiFaceLandmarks[0][tassellationDatabase[a][1]];
        let vertex3 = detections.multiFaceLandmarks[0][tassellationDatabase[a][2]];
    
        maskCanvas.vertex(vertex1.x * width, vertex1.y * height);
        maskCanvas.vertex(vertex2.x * width, vertex2.y * height);
        maskCanvas.vertex(vertex3.x * width, vertex3.y * height);
    }
    maskCanvas.endShape(CLOSE);
    maskCanvas.pop();

    
    faceCanvas.clear();
    faceCanvas.push();
    faceCanvas.translate(-width / 2, -height / 2);

    faceCanvas.beginShape(TRIANGLES);

    faceCanvas.texture(personPicture);
    for(a = 0; a < tassellationDatabase.length; a++){
        let sourceImageVertex1 = neutralDatabase[tassellationDatabase[a][0]];
        let sourceImageVertex2 = neutralDatabase[tassellationDatabase[a][1]];
        let sourceImageVertex3 = neutralDatabase[tassellationDatabase[a][2]];

        let vertex1 = detections.multiFaceLandmarks[0][tassellationDatabase[a][0]];
        let vertex2 = detections.multiFaceLandmarks[0][tassellationDatabase[a][1]];
        let vertex3 = detections.multiFaceLandmarks[0][tassellationDatabase[a][2]];
    
        faceCanvas.vertex(vertex1.x * width, vertex1.y * height, sourceImageVertex1.x * 720 + 720 * 4, sourceImageVertex1.y * height);
        faceCanvas.vertex(vertex2.x * width, vertex2.y * height, sourceImageVertex2.x * 720 + 720 * 4, sourceImageVertex2.y * height);
        faceCanvas.vertex(vertex3.x * width, vertex3.y * height, sourceImageVertex3.x * 720 + 720 * 4, sourceImageVertex3.y * height);
    }
    faceCanvas.endShape(CLOSE);
    faceCanvas.pop();

    pass1.shader(blurH);
    // send the camera texture to the horizontal blur shader
    // send the size of the texels
    // send the blur direction that we want to use
    blurH.setUniform('tex0', maskCanvas);
    blurH.setUniform('texelSize', [5.0 / width, 5.0 / height]);
    blurH.setUniform('direction', [1.0, 0.0]);

    pass1.rect(0,0,width, height);
    
    pass2.shader(blurV);
    // instead of sending the webcam, we will send our first pass to the vertical blur shader
    // texelSize remains the same as above
    // direction changes to [0.0, 1.0] to do a vertical pass
    blurV.setUniform('tex0', pass1);
    blurV.setUniform('texelSize', [5.0 / width, 5.0 / height]);
    blurV.setUniform('direction', [0.0, 1.0]);

    pass2.rect(0,0,width, height);


    maskBluredCanvas.shader(shaderMask);
    shaderMask.setUniform('tex0', faceCanvas);
    shaderMask.setUniform('tex1', pass2);
    maskBluredCanvas.rect(0,0,width, height);


    image(maskBluredCanvas, 0, 0);
}





